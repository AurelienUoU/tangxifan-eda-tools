%% SRAM MUX
% Size <= 4, use area, delay and power of one-level mux
% Size > 4, use area, delay and power of two-level mux

%% Area
area_sram_mux = 2:2:32;
pn_ratio = 1.9;
for i=1:1:length(area_sram_mux)
  area_multiplexing = (2*i) * (trans_area(2)+ trans_area(2*pn_ratio));
  if ( 2*i > 4) 
    area_multiplexing = area_multiplexing + ceil(sqrt(2*i)) * (trans_area(2)+ trans_area(2*pn_ratio));
  end 

  area_buf = (2*i + 1) * (trans_area(2)+ trans_area(2*pn_ratio));

  area_sram_mux(i) = area_multiplexing + area_buf;
end

